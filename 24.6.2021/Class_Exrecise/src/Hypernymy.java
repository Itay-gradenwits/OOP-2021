public class Hypernymy {
    String hypernym;
    String hypornym;
    public Hypernymy(String hypernym, String hypornym) {
        this.hypernym = hypernym;
        this.hypornym = hypornym;
    }

    public String getHypernymy() {
        return hypernym;
    }

    public String getHypornymy() {
        return hypornym;
    }
}
